'''3. Comments
Comments are notes in your code that Python ignores — they help humans understand the code.

Types:

Single-line comment: #
'''
# Multi-line comment: triple quotes """ ... """ or ''' ... ''' (though technically this is a string, not a true comment — but often used for docstrings or multi-line notes)
# Example:

# This is a single-line comment
print("Hello")  # Comment after code

